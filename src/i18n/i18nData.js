export function getSupportedLanguage(language) {
  return LANGUAGES.some((item) => item.code === language) ? language : 'en'
}

export function getCopy(language) {
  return SITE_COPY[getSupportedLanguage(language)]
}

export function getLocale(language) {
  return LANGUAGES.find((item) => item.code === getSupportedLanguage(language))?.locale || 'en-US'
}

export function localizeSkillset(skillset, language) {
  const names = getCopy(language).skills.names
  return skillset.map((item) => ({
    ...item,
    name: names[item.name] || item.name,
  }))
}

export function localizeProjects(projects, language) {
  if (getSupportedLanguage(language) === 'en') return projects

  return projects.map((project) => {
    const text = UZ_PROJECT_COPY[project.id]
    if (!text) return project

    return {
      ...project,
      ...text,
      collaborators: project.collaborators.map((member, index) => ({
        ...member,
        ...(text.collaborators?.[index] || {}),
      })),
      gallery: project.gallery?.map((shot, index) => ({
        ...shot,
        ...(text.gallery?.[index] || {}),
      })),
    }
  })
}
